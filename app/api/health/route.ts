// ============================================================================
// このAPI（GET /api/health）は「サーバー（会社の裏側のコンピューター）が生きて動いているか」を
// 確認するための窓口です。人間ではなく機械が使います。
//   ロードバランサ（アクセスを複数のサーバーに振り分ける装置）や監視ツールが、
//   このURLを定期的に呼び出し、ちゃんと応答（レスポンス＝応答）が返ってくれば「正常」と判断します。
//   健康診断の“脈があるか確認する”ようなイメージです。
// ============================================================================

// NextResponse = サーバーからの応答を作る道具
import { NextResponse } from "next/server";

// 呼び出されたら、状態は "ok"（正常）で、現在時刻(time)を付けて返すだけのシンプルな関数。
// Date.now() = 今の時刻を数値（1970年からの経過ミリ秒）で表したもの。
export async function GET() {
  return NextResponse.json({ status: "ok", time: Date.now() });
}
